def setup_document() -> Document:
    doc = Document()
    section = doc.sections[0]
    section.left_margin = Cm(3)
    section.right_margin = Cm(1.5)
    section.top_margin = Cm(2)
    section.bottom_margin = Cm(2)
    normal = doc.styles["Normal"]
    normal.font.name = "Times New Roman"
    normal._element.rPr.rFonts.set(qn("w:eastAsia"), "Times New Roman")
    normal.font.size = Pt(14)
    normal.paragraph_format.first_line_indent = Cm(1.25)
    normal.paragraph_format.line_spacing = 1.0
    for style_name in ("Heading 1", "Heading 2", "Heading 3"):
        style = doc.styles[style_name]
        style.font.name = "Times New Roman"
        style._element.rPr.rFonts.set(qn("w:eastAsia"), "Times New Roman")
        style.font.size = Pt(14)
        style.font.bold = True
    return doc


def p(doc: Document, text: str, center: bool = False, no_indent: bool = False) -> None:
    para = doc.add_paragraph(text)
    para.alignment = WD_ALIGN_PARAGRAPH.CENTER if center else WD_ALIGN_PARAGRAPH.JUSTIFY
    if no_indent:
        para.paragraph_format.first_line_indent = Cm(0)


def h(doc: Document, text: str, level: int = 1, center: bool = False) -> None:
    para = doc.add_paragraph(text, style=f"Heading {level}")
    para.alignment = WD_ALIGN_PARAGRAPH.CENTER if center else WD_ALIGN_PARAGRAPH.LEFT
    para.paragraph_format.first_line_indent = Cm(0)


def bullets(doc: Document, items: list[str]) -> None:
    for item in items:
        p(doc, f"- {item}", no_indent=True)


def table(doc: Document, title: str, headers: list[str], rows: list[list[str]], size: int = 10) -> None:
    p(doc, title, no_indent=True)
    t = doc.add_table(rows=1, cols=len(headers))
    t.style = "Table Grid"
    for idx, value in enumerate(headers):
        cell = t.rows[0].cells[idx]
        cell.text = ""
        run = cell.paragraphs[0].add_run(value)
        run.bold = True
        run.font.name = "Times New Roman"
        run._element.rPr.rFonts.set(qn("w:eastAsia"), "Times New Roman")
        run.font.size = Pt(size)
    for row in rows:
        cells = t.add_row().cells
        for idx, value in enumerate(row):
            cells[idx].text = ""
            run = cells[idx].paragraphs[0].add_run(value)
            run.font.name = "Times New Roman"
            run._element.rPr.rFonts.set(qn("w:eastAsia"), "Times New Roman")
            run.font.size = Pt(size)


def code_block(doc: Document, title: str, text: str) -> None:
    p(doc, title, no_indent=True)
    for line in text.splitlines():
        para = doc.add_paragraph()
        para.alignment = WD_ALIGN_PARAGRAPH.LEFT
        para.paragraph_format.first_line_indent = Cm(0)
        run = para.add_run(line if line else " ")
        run.font.name = "Courier New"
        run._element.rPr.rFonts.set(qn("w:eastAsia"), "Courier New")
        run.font.size = Pt(9)


def page_break(doc: Document) -> None:
    doc.add_page_break()


def snippet(path: Path, start: int, end: int) -> str:
    lines = path.read_text(encoding="utf-8-sig").splitlines()
    return "\n".join(lines[start - 1:end])


